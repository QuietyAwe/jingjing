/** 常用中文停用词表（约 120 词） */
const STOP_WORDS = new Set([
  // 人称代词
  "我", "你", "他", "她", "它", "我们", "你们", "他们", "自己", "大家",
  // 语气词 / 助词
  "的", "了", "着", "过", "地", "得", "呢", "吧", "啊", "呀", "哦", "嘛", "哈",
  "么", "吗", "啦", "唉", "嗯", "哎", "嗨",
  // 介词 / 连词
  "在", "是", "有", "和", "与", "或", "及", "等", "到", "从", "把", "被",
  "对", "向", "给", "让", "比", "因", "因为", "所以", "但是", "而且", "如果",
  "虽然", "不过", "还是", "或者", "以及", "然后", "接着", "于是",
  // 副词
  "就", "也", "都", "还", "又", "再", "才", "很", "太", "非常", "特别",
  "已经", "正在", "将要", "可能", "大概", "也许", "不", "没", "没有",
  // 量词 / 指示词
  "这", "那", "这个", "那个", "这些", "那些", "每", "各", "某",
  "一", "二", "三", "四", "五", "六", "七", "八", "九", "十",
  "一些", "一点", "一下", "一样", "一般",
  // 疑问词
  "什么", "怎么", "怎样", "哪里", "哪儿", "哪个", "哪些",
  "为什么", "几", "多少", "谁", "如何",
  // 常用动词（太泛化无检索意义）
  "是", "有", "做", "说", "想", "看", "来", "去", "走", "会",
  "能", "可以", "应该", "要", "得", "需要", "喜欢",
  // 时间词
  "今天", "昨天", "明天", "现在", "刚才", "以前", "以后", "最近",
  "时候", "小时", "分钟",
  // 其他高频虚词
  "上", "下", "里", "中", "内", "外", "前", "后", "左", "右",
  "大", "小", "多", "少", "好", "坏", "新", "旧",
  "起来", "出来", "过来", "下去", "上去", "过来",
]);

/**
 * 纯 JS 中文分词
 * 策略：正则提取中文词片段（2-6 字）+ 英文单词，过滤停用词，去重后取前 N 个
 */
export function tokenize(text: string, maxKeywords: number = 3): string[] {
  // 1. 提取中文词片段（连续汉字 2-6 字）和英文单词
  const cjkChunks = text.match(/[一-鿿]{2,6}/g) || [];
  const engWords = text.match(/[a-zA-Z]{2,}/g) || [];

  const candidates = [...cjkChunks, ...engWords.map((w) => w.toLowerCase())];

  // 2. 过滤停用词 + 去重
  const seen = new Set<string>();
  const keywords: string[] = [];

  for (const word of candidates) {
    if (STOP_WORDS.has(word)) continue;
    if (seen.has(word)) continue;
    seen.add(word);
    keywords.push(word);
  }

  // 3. 截断到 maxKeywords
  return keywords.slice(0, maxKeywords);
}
